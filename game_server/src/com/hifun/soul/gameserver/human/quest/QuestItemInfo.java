public class QuestItemInfo {
	private int itemId;
	private int itemIcon;

	public int getItemId() {
		return itemId;
	}

	public void setItemId(int itemId) {
		this.itemId = itemId;
	}

	public int getItemIcon() {
		return itemIcon;
	}

	public void setItemIcon(int itemIcon) {
		this.itemIcon = itemIcon;
	}
}
