package com.hifun.soul.gameserver.cd;

public class CdInfo {

	private int cdType;
	
	private int times;
	
	private long endTime;
	
	public int getCdType() {
		return cdType;
	}
	public void setCdType(int cdType) {
		this.cdType = cdType;
	}
	public int getTimes() {
		return times;
	}
	public void setTimes(int times) {
		this.times = times;
	}
	public long getEndTime() {
		return endTime;
	}
	public void setEndTime(long endTime) {
		this.endTime = endTime;
	}
	
}
