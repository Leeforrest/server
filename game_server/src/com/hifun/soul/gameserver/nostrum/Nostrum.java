package com.hifun.soul.gameserver.nostrum;

public class Nostrum {
	private int propertyId;
	private int propertyAmendRate;
	private int propertyAmendMethod;
	private long endTime;

	public int getPropertyId() {
		return propertyId;
	}

	public void setPropertyId(int propertyId) {
		this.propertyId = propertyId;
	}

	public int getPropertyAmendRate() {
		return propertyAmendRate;
	}

	public void setPropertyAmendRate(int propertyAmendRate) {
		this.propertyAmendRate = propertyAmendRate;
	}

	public int getPropertyAmendMethod() {
		return propertyAmendMethod;
	}

	public void setPropertyAmendMethod(int propertyAmendMethod) {
		this.propertyAmendMethod = propertyAmendMethod;
	}

	public long getEndTime() {
		return endTime;
	}

	public void setEndTime(long endTime) {
		this.endTime = endTime;
	}

}
